package FunSimple;

public class FunSimple18 {
    public static void main(String[] args) {
        System.out.println(area(2));
        System.out.println(area(3));
        System.out.println(area(5));
    }

    static double area(double a) {
        double s;
        s = 3.14 * a * a;
        return s;
    }
}
